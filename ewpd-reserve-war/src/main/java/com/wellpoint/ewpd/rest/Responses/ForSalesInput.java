package com.wellpoint.ewpd.rest.Responses;

public class ForSalesInput {
	
	
	String baseContract;
    String modifiedByP2A;
    String hpcc;
    String fundingType;
    String state;
    String groupSize;
    
    
	/**
	 * @return the baseContract
	 */
	public String getBaseContract() {
		return baseContract;
	}
	/**
	 * @param baseContract the baseContract to set
	 */
	public void setBaseContract(String baseContract) {
		this.baseContract = baseContract;
	}
	/**
	 * @return the modifiedByP2A
	 */
	public String getModifiedByP2A() {
		return modifiedByP2A;
	}
	/**
	 * @param modifiedByP2A the modifiedByP2A to set
	 */
	public void setModifiedByP2A(String modifiedByP2A) {
		this.modifiedByP2A = modifiedByP2A;
	}
	/**
	 * @return the hpcc
	 */
	public String getHpcc() {
		return hpcc;
	}
	/**
	 * @param hpcc the hpcc to set
	 */
	public void setHpcc(String hpcc) {
		this.hpcc = hpcc;
	}
	/**
	 * @return the fundingType
	 */
	public String getFundingType() {
		return fundingType;
	}
	/**
	 * @param fundingType the fundingType to set
	 */
	public void setFundingType(String fundingType) {
		this.fundingType = fundingType;
	}
	/**
	 * @return the state
	 */
	public String getState() {
		return state;
	}
	/**
	 * @param state the state to set
	 */
	public void setState(String state) {
		this.state = state;
	}
	/**
	 * @return the groupSize
	 */
	public String getGroupSize() {
		return groupSize;
	}
	/**
	 * @param groupSize the groupSize to set
	 */
	public void setGroupSize(String groupSize) {
		this.groupSize = groupSize;
	}
        

}
